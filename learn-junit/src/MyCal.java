public class MyCal {
    public float add(float a, float b){
        return  a + b;
    }

    public float minus(float a, float b){
        return  a - b;
    }

    public float mul(float a, float b){
        return  a * b;
    }

    public float div(float a, float b){
        return  a / b;
    }

    public float min(float a, float b){
        return  (a < b) ? a : b;
    }

    public float max(float a, float b){
        return  (a > b) ? a : b;
    }
}
